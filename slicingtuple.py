a=((10,20,30),
   (3,4,5),
   (6,7,8),)
print("original tuple")
print(a)
print()

print("print the first slicing tuple ")
x=a[0:3]
print(x)
print()

print("print the next tuple in the slicing")
s=a[0:]
print(s)
print()

print("the slicing of tuple in the 2 postion ")
d=a[:1]
print(d)
print()

print("the slicing in the reverse order")
f=a[-2:]
print(f)
print()



